/* ── Invoice generator using jsPDF from CDN window.jspdf ─────────────────── */
const downloadInvoice = (booking, event) => {
  const { jsPDF } = window.jspdf || {};
  if (!jsPDF) {
    const w = window.open('', '_blank');
    w.document.write(`
      <html><head><title>Invoice</title>
      <style>
        body { font-family: Arial,sans-serif; padding: 40px; color: #111; }
        h1 { color: #D4AF37; } hr { border-color: #D4AF3733; }
        .row { display: flex; justify-content: space-between; margin: 8px 0; }
        .label { color: #666; } .val { font-weight: 600; }
        .total { background: #fff8e1; border: 1px solid #D4AF37; padding: 12px 16px; border-radius: 8px; }
      </style></head><body>
      <h1>EventPro — Booking Invoice</h1>
      <hr/>
      <p><strong>Booking ID:</strong> ${booking.id}</p>
      <p><strong>Event:</strong> ${event.title}</p>
      <p><strong>Date:</strong> ${event.date} at ${event.startTime}</p>
      <p><strong>Location:</strong> ${event.location}</p>
      <hr/>
      <p><strong>Customer:</strong> ${booking.customerName || ''}</p>
      <p><strong>Email:</strong> ${booking.customerEmail || ''}</p>
      <p><strong>Phone:</strong> ${booking.customerPhone || ''}</p>
      <hr/>
      <p><strong>Tickets:</strong> ${booking.ticketCount}</p>
      <p><strong>Price per Ticket:</strong> ₹${event.price}</p>
      <p><strong>Total Amount:</strong> ₹${booking.totalAmount}</p>
      <p><strong>Payment Status:</strong> ${(booking.paymentStatus || 'confirmed').toUpperCase()}</p>
      <hr/>
      <p style="color:#888;font-size:12px">Generated by EventPro on ${new Date().toLocaleDateString('en-IN')}</p>
      </body></html>
    `);
    w.document.close();
    w.print();
    return;
  }
  // (Full jsPDF logic remains the same)
  const doc = new jsPDF();
  const pageW = doc.internal.pageSize.getWidth();
  doc.setFillColor(15, 15, 26);
  doc.rect(0, 0, pageW, 45, 'F');
  doc.setFillColor(201, 168, 76);
  doc.rect(0, 43, pageW, 2, 'F');
  doc.setTextColor(245, 210, 112);
  doc.setFontSize(22);
  doc.setFont('helvetica', 'bold');
  doc.text('EventPro', 15, 20);
  doc.setFontSize(10);
  doc.setTextColor(150, 150, 150);
  doc.setFont('helvetica', 'normal');
  doc.text('BOOKING INVOICE', 15, 30);
  doc.setTextColor(245, 210, 112);
  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.text(`Invoice #${booking.id}`, pageW - 15, 20, { align: 'right' });
  doc.setTextColor(150, 150, 150);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  doc.text(new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' }), pageW - 15, 30, { align: 'right' });
  let y = 58;
  doc.setFillColor(30, 30, 58);
  doc.roundedRect(10, y - 6, pageW - 20, 42, 3, 3, 'F');
  doc.setTextColor(201, 168, 76);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.text('EVENT DETAILS', 16, y + 2);
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(13);
  doc.text(event.title, 16, y + 12);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(170, 170, 170);
  doc.text(`Date: ${event.date}  |  Time: ${event.startTime}  |  Location: ${event.location}`, 16, y + 22);
  y += 54;
  doc.setFillColor(22, 22, 44);
  doc.roundedRect(10, y - 6, pageW - 20, 52, 3, 3, 'F');
  doc.setTextColor(201, 168, 76);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.text('CUSTOMER INFORMATION', 16, y + 2);
  const custRows = [
    ['Name', booking.customerName || '—'],
    ['Email', booking.customerEmail || '—'],
    ['Phone', booking.customerPhone || '—'],
    ['Location', [booking.customerCity, booking.customerState, booking.customerCountry].filter(Boolean).join(', ') || '—'],
  ];
  custRows.forEach(([label, val], idx) => {
    const ry = y + 12 + idx * 9;
    doc.setTextColor(140, 140, 140);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.text(label, 16, ry);
    doc.setTextColor(220, 220, 220);
    doc.text(val, 70, ry);
  });
  y += 64;
  doc.setFillColor(20, 20, 40);
  doc.roundedRect(10, y - 6, pageW - 20, 38, 3, 3, 'F');
  doc.setTextColor(201, 168, 76);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.text('TICKET SUMMARY', 16, y + 2);
  const tickRows = [
    ['Tickets Booked', `${booking.ticketCount}`],
    ['Price per Ticket', `Rs. ${event.price}`],
    ['Payment Status', (booking.paymentStatus || 'Confirmed').toUpperCase()],
  ];
  tickRows.forEach(([label, val], idx) => {
    const ry = y + 12 + idx * 8;
    doc.setTextColor(140, 140, 140);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.text(label, 16, ry);
    doc.setTextColor(220, 220, 220);
    doc.text(val, 120, ry);
  });
  y += 50;
  doc.setFillColor(201, 168, 76);
  doc.roundedRect(10, y, pageW - 20, 22, 4, 4, 'F');
  doc.setTextColor(15, 15, 26);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(12);
  doc.text('TOTAL AMOUNT', 16, y + 14);
  doc.setFontSize(14);
  doc.text(`Rs. ${Number(booking.totalAmount).toFixed(2)}`, pageW - 16, y + 14, { align: 'right' });
  y += 34;
  doc.setTextColor(80, 80, 80);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(8);
  doc.text('Thank you for booking with EventPro. This is your official booking confirmation.', pageW / 2, y, { align: 'center' });
  doc.text('For support: support@eventpro.com', pageW / 2, y + 6, { align: 'center' });
  doc.save(`EventPro_Invoice_${booking.id}.pdf`);
};

